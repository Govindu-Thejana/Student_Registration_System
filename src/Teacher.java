private boolean validateInputs() {
    if (id.getText().isEmpty() || name.getText().isEmpty() || address.getText().isEmpty() ||
            age.getText().isEmpty() || birthday.getText().isEmpty() || contact.getText().isEmpty() ||
            gender.getSelectedItem().toString().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill in all fields.");
        return false;
    }

    try {
        Integer.parseInt(id.getText());
        Integer.parseInt(age.getText());
        Integer.parseInt(contact.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Please enter valid numbers for ID, Age, and Contact.");
        return false;
    }

    return true;
}


