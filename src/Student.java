    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
       try {
        stmt = con.createStatement();

        int sId = Integer.parseInt(id.getText());
        String sName = name.getText();
        String sAddress = address.getText();
        String sGrade = (String) grade.getSelectedItem();
        int sAge = Integer.parseInt(age.getText());
        long birthdayMillis = Date.parse(birthday.getText());
        java.sql.Date sBirthday = new java.sql.Date(birthdayMillis);
        String sGender = (String) gender.getSelectedItem();
        long admissionMillis = Date.parse(admission.getText());
        java.sql.Date sAdmission = new java.sql.Date(admissionMillis);
        String sGuardian = guardian.getText();
        int sContact = Integer.parseInt(contact.getText());

        
            String query = "INSERT INTO student (id, name, address, grade, age, birthday, gender, admission, guardian, contact, image) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            PreparedStatement preparedStmt = con.prepareStatement(query);
            preparedStmt.setInt(1, sId);
            preparedStmt.setString(2, sName);
            preparedStmt.setString(3, sAddress);
            preparedStmt.setString(4, sGrade);
            preparedStmt.setInt(5, sAge);
            preparedStmt.setDate(6, sBirthday);
            preparedStmt.setString(7, sGender);
            preparedStmt.setDate(8, sAdmission);
            preparedStmt.setString(9, sGuardian);
            preparedStmt.setInt(10, sContact);
            preparedStmt.setBytes(11, photo);

            preparedStmt.execute();
            JOptionPane.showMessageDialog(null, "Successfully ADDED");
        
        }
       catch (HeadlessException | NumberFormatException | SQLException  e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error occurred while adding the teacher.");
        }        
    }//GEN-LAST:event_submitActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try {
        stmt = con.createStatement();

        int sId = Integer.parseInt(id.getText());

        String query = "SELECT * FROM student";

        rs = stmt.executeQuery(query);

        while (rs.next()) {
            if (sId == rs.getInt("id")) {
                name.setText(rs.getString("name"));
                address.setText(rs.getString("address"));
                grade.setSelectedItem(rs.getString("grade"));
                age.setText(String.format("%s", rs.getInt("age")));
                birthday.setText(String.format("%tD", rs.getDate("birthday")));
                gender.setSelectedItem(rs.getString("gender"));
                admission.setText(String.format("%tD", rs.getDate("admission")));
                guardian.setText(rs.getString("guardian"));
                contact.setText(String.format(0 + "%s", rs.getInt("contact")));

                BufferedImage image = ImageIO.read(rs.getBinaryStream("image"));
                imageIcon.setIcon(new ImageIcon(image));
            }
        }
    } catch (NumberFormatException | SQLException | IOException e) {
        String errorMessage = "Error occurred while searching for student data: " + e.getMessage();
        Logger.getLogger(Student.class.getName()).log(Level.SEVERE, errorMessage, e);
        JOptionPane.showMessageDialog(this, "An error occurred while searching for student data. Please try again later.", "Search Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_searchActionPerformed

