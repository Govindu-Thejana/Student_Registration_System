    private void selectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectActionPerformed
        // TODO add your handling code here:
   JFileChooser chooser = new JFileChooser();
    chooser.showOpenDialog(null);
    File file = chooser.getSelectedFile();
    if (file != null) {
        imageIcon.setIcon(new ImageIcon(file.toString()));
        filename = file.getAbsolutePath();

        try {
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for (int readnum; (readnum = fis.read(buf)) != -1; ) {
                bos.write(buf, 0, readnum);
            }
            photo = bos.toByteArray();
        } catch (IOException e) {
            String errorMessage = "Error occurred while reading image file: " + e.getMessage();
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, errorMessage, e);
            JOptionPane.showMessageDialog(this, "An error occurred while reading the image file. Please try again later.", "Image Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_selectActionPerformed

